package StringPreparation;

public class StringMethods {
    public static void main(String[] args) {
        String strOne = "venkatesh999";
        System.out.println("String = "+strOne);
        System.out.println("Length of String = "+strOne.length());
        System.out.println("Upper case of String = "+strOne.toUpperCase());
        System.out.println("Lower case of String = "+strOne.toLowerCase());
        System.out.println("Character position = "+strOne.charAt(4));
        System.out.println("index number = "+strOne.lastIndexOf('e'));



    }
}
